def add_ai_role_to_frontmatter(content: str, ai_role: str) -> str:
    """frontmatter의 category: 줄 뒤에 ai_role: 추가"""
    # 이미 ai_role이 있으면 스킵
    if "ai_role:" in content:
        return content
    # category: 줄 뒤에 삽입
    return re.sub(
        r"(category: .+\n)",
        rf"\1ai_role: {ai_role}\n",
        content,
        count=1
    )


def append_sections(content: str, sections: dict) -> str:
    """파일 끝에 섹션 추가 (이미 존재하면 스킵)"""
    result = content.rstrip("\n") + "\n"

    for key, text in sections.items():
        header = text.split("\n")[0]  # e.g. "## AI 역할"
        if header not in result:
            result += "\n" + text.strip() + "\n"

    return result


def process_file(filepath: Path, update: dict) -> bool:
    """파일 처리 후 True 반환 (변경 없으면 False)"""
    original = filepath.read_text(encoding="utf-8")

    content = add_ai_role_to_frontmatter(original, update["ai_role"])

    sections = {
        "ai": update["ai_section"],
        "biz": update["biz_section"],
        "tam": update["tam_section"],
        "convenience": update["convenience_section"],
    }
    content = append_sections(content, sections)

    if content == original:
        return False

    filepath.write_text(content, encoding="utf-8")
    return True


def main():
    changed = []
    skipped = []

    for filename, update in UPDATES.items():
        filepath = IDEAS_DIR / filename
        if not filepath.exists():
            print(f"[MISSING] {filename}")
            continue

        if process_file(filepath, update):
            changed.append(filename)
            print(f"[OK] {filename} (ai_role={update['ai_role']})")
        else:
            skipped.append(filename)
            print(f"[SKIP] {filename} (이미 업데이트됨)")

    print(f"\n완료: {len(changed)}개 변경, {len(skipped)}개 스킵")

    # ai-only 카테고리별 요약
    print("\n[ai-only 배정 요약]")
    for fname, upd in UPDATES.items():
        if upd["ai_role"] == "ai-only":
            print(f"  - {fname}")


if __name__ == "__main__":
    main()
